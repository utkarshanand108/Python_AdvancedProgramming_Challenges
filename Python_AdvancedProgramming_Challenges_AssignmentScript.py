def primes_upto(n: int):
    if n < 2:
        return []
    sieve = [True] * (n + 1)
    sieve[0] = sieve[1] = False
    p = 2
    while p * p <= n:
        if sieve[p]:
            for m in range(p * p, n + 1, p):
                sieve[m] = False
        p += 1
    return [i for i, is_prime in enumerate(sieve) if is_prime]

# Demo
n = int(input("Enter n: "))
print("Primes up to n:", primes_upto(n))

