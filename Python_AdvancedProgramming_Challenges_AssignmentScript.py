# Task 1
def count_vowels_consonants(s: str):
    vowels = set("aeiou")
    v = c = 0
    for ch in s.lower():
        if ch.isalpha():
            if ch in vowels:
                v += 1
            else:
                c += 1
    return v, c

# Demo
text = input("Enter text: ")
v, c = count_vowels_consonants(text)
print("Vowels:", v, "Consonants:", c)

